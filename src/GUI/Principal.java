    private void CB_BDTablasItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_CB_BDTablasItemStateChanged
        this.CB_Columnas.removeAll();
        this.CB_Columnas.removeAllItems();
        if (this.CB_BDTablas.getSelectedItem() != null) {
            try {
                this.CB_Columnas.removeAll();
                String ta = this.CB_BDTablas.getSelectedItem().toString();
                for (Object a : dao.getColumnasFromTable(ta).toArray()) {
                    this.CB_Columnas.addItem(a.toString());
                }
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_CB_BDTablasItemStateChanged

