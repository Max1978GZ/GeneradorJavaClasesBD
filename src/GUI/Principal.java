    private void cargarCadenaConexion() {
        svr.setBasedatos(this.CBOBD.getSelectedItem().toString());

        svr.setPassword(this.TxtPassword.getText());
        svr.setUsuario(this.TxtUser.getText());
        svr.setIp("localhost");
//        Parametros.conf conf = new Parametros.conf();
        switch (TipoServidor.valueOf(this.CBTipoServidor.getSelectedItem().toString())) {
            case derby:                
                svr.setPuerto("1527");
                break;
            case mysql:
            case mariadb:    
              svr.setPuerto("3306");
                break;
            case oracle:
                svr.setPuerto("1521");
                break;

        }
        svr.getDriverClass( TipoServidor.valueOf(this.CBTipoServidor.getSelectedItem().toString()));

        this.TxtCadenaConexion.setText(svr.getCadenaConexion());
        //Parametros.conf.strconex= this.TxtCadenaConexion.getText();

    }

